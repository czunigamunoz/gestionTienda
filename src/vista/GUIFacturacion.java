    private int esNumero(String entrada){
        int numero;
        try{
            numero = Integer.parseInt(entrada);
            return numero;
        }catch(NumberFormatException nfe){
            return -1;
        }
    }
    
    private void mostrarProductos(){
        data = control.mostrarProductoPedido();
        dtm = new DefaultTableModel(data, columnas);
        tablaProductosPedido.setModel(dtm);
        tablaProductosPedido.repaint();
    }
    
    private void iniciarFactura(){
        control.setCodigo(++codigo);
        txtFactura.setText(""+control.obtenerCodigo());
        txtFecha.setText(fechaSimple.format(fecha));
    }
    
    private void descuento(){
        float descuento, iva;
        double totalDescuento, total, sub;
        if(!txtDescuento.getText().isEmpty()){
            obtenerValores();
            total = totalConIva;
            sub = totalSinIva;
            descuento = (float) esNumero(txtDescuento.getText())/100;
            totalDescuento = (total*descuento);
            total -= totalDescuento;
            if(total >=0){
                iva  = (float) (total*IVA);
                sub -= iva;
                String subTotalFormat = String.format("%.0f", sub);
                String ivaProductoFormat = String.format("%.0f", iva);
                String totalFormat = String.format("%.0f", total);
                txtSubtotal.setText(""+subTotalFormat);
                txtIva.setText(""+ivaProductoFormat);
                txtTotal.setText(""+totalFormat);
            }
        }
    }
    
    private void obtenerValores(){
        totalConIva = Double.parseDouble(txtTotal.getText());
        totalSinIva = Double.parseDouble(txtSubtotal.getText());
        ivaActual = Float.parseFloat(txtIva.getText());
    }
    
    private void cambio(){
        double efectivo, cambio;
        if(!txtEfectivo.getText().isEmpty()){
            obtenerValores();
            efectivo = (double) esNumero(txtEfectivo.getText());
            if(efectivo > totalConIva){
                cambio = efectivo - totalConIva;
                String cambioFormat = String.format("%.0f", cambio);
                txtCambio.setText(cambioFormat);
            }else{
                txtCambio.setText(""+0);
            }            
        }
    }
    
