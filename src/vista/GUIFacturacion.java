    private void btnEliminarProActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarProActionPerformed
        // TODO add your handling code here:
        int filaSelecionada, respuestaOpcion, cantidadSelecionada;
        
        filaSelecionada = tablaProductosPedido.getSelectedRow();
        if(filaSelecionada == -1){
           JOptionPane.showMessageDialog(this, "Debe Seleccionar un producto a eliminar","Advertencia",JOptionPane.WARNING_MESSAGE); 
        }else{            
            respuestaOpcion = JOptionPane.showConfirmDialog(null, "¿Estas seguro de eliminar la cantidad asignada de este producto","Eliminar",JOptionPane.YES_NO_OPTION);
             if(respuestaOpcion == JOptionPane.YES_OPTION){
                cantidadSelecionada = esNumero(txtCantidad.getText());
                nombre = tablaProductosPedido.getValueAt(filaSelecionada, 1).toString();
                producto = control.buscarProductoPedido(nombre);
                if(cantidadSelecionada == producto.getCantidad()){
                    codigo = Integer.parseInt(tablaProductosPedido.getValueAt(filaSelecionada, 0).toString());
                    boolean elimino = control.eliminarProductoPedido(codigo);
                    if(elimino){
                        eliminarDetalleProducto(filaSelecionada);
                    }else{
                        JOptionPane.showMessageDialog(this, "El producto no pudo ser eliminado");
                    }
                }else{
                    producto.setCantidad(producto.getCantidad() - Integer.parseInt(txtCantidad.getText()));
                    eliminarDetalleProducto(filaSelecionada);
                }                    
            }
        }
    }//GEN-LAST:event_btnEliminarProActionPerformed

    private void eliminarDetalleProducto(int filaSelecionada){
        double precioProducto, totalEliminado;        
        precioProducto = Double.parseDouble(tablaProductosPedido.getValueAt(filaSelecionada,2).toString()) * Integer.parseInt(txtCantidad.getText());
        totalEliminado = Double.parseDouble(txtTotal.getText()) - precioProducto;
        totalConIva = totalEliminado;
        String totalConIvaString = String.format("%.0f", totalConIva);
        txtTotal.setText(totalConIvaString);
        ivaActual = (float) (totalConIva*IVA);
        String ivaActualString = String.format("%.0f", ivaActual);
        txtIva.setText(ivaActualString);
        totalSinIva = totalEliminado - ivaActual;
        String totalSinIvaString = String.format("%.0f", totalSinIva);
        txtSubtotal.setText(totalSinIvaString);
        mostrarProductos();
        obtenerValores();
    }
    
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {
            obtenerValores();
            fechaFacutra = txtFecha.getText();
            if(cliente == null){
            JOptionPane.showMessageDialog(this, "Debe selecionar un cliente primero");
            }else{
                Factura factura = new Factura(fecha, totalSinIva, ivaActual, totalConIva, cliente);
                boolean agregada = control.agregarFactura(factura);
                if(agregada){
                    JOptionPane.showMessageDialog(this, "Fecha: " + fechaFacutra + "\n\n"
                            + "Atendido por: " + txtAtendido.getText()
                            + "Factura numero: " + control.obtenerCodigo() + " generada con exito\n\n"
                            + "A nombre de: " + cliente.getNombres() + " " + cliente.getApellidos() + "\n\n"
                            + "Subtotal de: " + totalSinIva + "\n\n"
                            + "Iva de: " + ivaActual + "\n\n"
                            + "Para un total de: " + totalConIva + "\n\n");
                    dispose();
                }else{
                    JOptionPane.showMessageDialog(this, "No se pudo agregar factura, rectificar campos");
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Debe agregar al menos un producto");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

